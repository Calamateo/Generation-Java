
public class While {

	public static void main(String[] args) {
		
		int i = 0 ;
		
		while(i<5) {
			
			for (int j =0; j <5;j++) {
				System.out.println(i+""+j);
			}
			i++;
		}
		
int[] numbers = {1,2,3,4,5};
		
		for(float number:numbers) {
		
		System.out.println(number);

	}


	}

}
